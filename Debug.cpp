// 静的メンバ
//=============================================================================
char CDebug::m_Text[TEXT_MAX] = {};
LPD3DXFONT CDebug::m_pFont = NULL;

//*************************************************************************************************
// テキストクリア
//*************************************************************************************************
void CDebug::ClearText(void)
{
    strcpy(m_Text,"");
}
//*************************************************************************************************
// テキスト追加
//*************************************************************************************************
void CDebug::AddText(char *Text)
{
    strcat(m_Text,Text);
}
//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
void CDebug::Init(void)
{
	// デバッグ情報表示用フォントの生成
	D3DXCreateFont(CManager::GetRenderer()->GetDevice(), 14, 7, 0, 0, FALSE, SHIFTJIS_CHARSET,
		OUT_DEFAULT_PRECIS, DEFAULT_QUALITY, DEFAULT_PITCH, "MS UI Gothic", &m_pFont);
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CDebug::Uninit(void)
{
	if (m_pFont != NULL)
	{
		m_pFont->Release();
		m_pFont = NULL;
	}
}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CDebug::Draw(void)
{
    RECT rect = {0, 0, SCREEN_WIDTH, SCREEN_HEIGHT};

    m_pFont->DrawText(NULL, m_Text, -1, &rect, DT_LEFT, D3DCOLOR_ARGB(0xdd, 0x00, 0xff, 0x00));
}
