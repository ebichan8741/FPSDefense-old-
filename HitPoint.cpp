//=================================================================================================
// 定数定義
//=================================================================================================
#define HP_POS_X	(50.0f)		// X座標
#define HP_POS_Y	(900.0f)	// Y座標
#define HP_SIZE_X	(50.0f)		// 幅
#define HP_SIZE_Y	(20.0f)		// 高さ

//=================================================================================================
// 構造体宣言
//=================================================================================================

//=================================================================================================
// グローバル変数
//=================================================================================================

//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CHitPoint::CHitPoint(int nPriolity) :CScene2D(nPriolity)
{
}

//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CHitPoint::Init(void)
{
	m_vPos = D3DXVECTOR3(HP_POS_X, HP_POS_Y, 0.0f);
	m_vSize = D3DXVECTOR3(HP_SIZE_X, HP_SIZE_Y, 0.0f);

	CScene2D::Init();
	return S_OK;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CHitPoint::Uninit(void)
{
	CScene2D::Uninit();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CHitPoint::Update(void)
{

}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CHitPoint::Draw(void)
{
	CScene2D::Draw();
}

//*************************************************************************************************
// インスタンス生成
//*************************************************************************************************
CHitPoint *CHitPoint::Create(void)
{
	CHitPoint *hitPoint;
	hitPoint = new CHitPoint(PRIOLITY_2);
	hitPoint->Init();
    hitPoint->m_pTexture = CManager::GetTextureManager()->getResource(CManager::GetTextureManager()->getTextureName(TEXTURE_HP));

	return hitPoint;
}
