//*************************************************************************************************
// 球と球の当たり判定
//*************************************************************************************************
bool HitSphereToSphere(D3DXVECTOR3 pos1,float len1,D3DXVECTOR3 pos2,float len2)
{
    D3DXVECTOR3 dir = pos1 - pos2;
    float length = D3DXVec3LengthSq(&dir);
    if(length < (len1 + len2))
    {
        return true;
    }
    return false;
}
