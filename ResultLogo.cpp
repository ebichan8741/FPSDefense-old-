#include "renderer.h"
#include "scene2D.h"
#include "ResultLogo.h"
#include "TextureManager.h"

//=================================================================================================
// 定数定義
//=================================================================================================
#define RESULTLOGO_POS_X	(640.0f)		// X座標
#define RESULTLOGO_POS_Y	(270.0f)	    // Y座標
#define RESULTLOGO_SIZE_X	(500.0f)		// 幅
#define RESULTLOGO_SIZE_Y	(200.0f)		// 高さ

//=================================================================================================
// 構造体宣言
//=================================================================================================

//=================================================================================================
// グローバル変数
//=================================================================================================

//*************************************************************************************************
// コンストラクタ
//*************************************************************************************************
CResultLogo::CResultLogo(int nPriolity) :CScene2D(nPriolity)
{
}

//*************************************************************************************************
// 初期化処理
//*************************************************************************************************
HRESULT CResultLogo::Init(void)
{
    m_vPos = D3DXVECTOR3(RESULTLOGO_POS_X, RESULTLOGO_POS_Y, 0.0f);
    m_vSize = D3DXVECTOR3(RESULTLOGO_SIZE_X, RESULTLOGO_SIZE_Y, 0.0f);

    CScene2D::Init();

    return S_OK;
}

//*************************************************************************************************
// 終了処理
//*************************************************************************************************
void CResultLogo::Uninit(void)
{
    CScene2D::Uninit();
}

//*************************************************************************************************
// 更新処理
//*************************************************************************************************
void CResultLogo::Update(void)
{

}

//*************************************************************************************************
// 描画処理
//*************************************************************************************************
void CResultLogo::Draw(void)
{
    CScene2D::Draw();
}

//*************************************************************************************************
// インスタンス生成
//*************************************************************************************************
CResultLogo *CResultLogo::Create(void)
{
    CResultLogo *resultLogo;
    resultLogo = new CResultLogo(PRIOLITY_2);
    resultLogo->Init();

    //resultLogo->LoadTexture(TEXTURE_RESULTLOGO);
    resultLogo->m_pTexture = CManager::GetTextureManager()->getResource(CManager::GetTextureManager()->getTextureName(TEXTURE_RESULTLOGO));

    return resultLogo;
}
